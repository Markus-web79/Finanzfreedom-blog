// checkContent.js
import fs from "fs";

const dictionaryPath = "./cspell.json";

// WÃ¶rterbuch laden oder neu anlegen
let dictionary = { words: [] };
if (fs.existsSync(dictionaryPath)) {
  dictionary = JSON.parse(fs.readFileSync(dictionaryPath, "utf-8"));
}

// Standard-Korrekturen fÃ¼r Umlaute und typische Fehler
const replacements = {
  "SteÃ¼r": "Steuer",
  "SteÃ¼rn": "Steuern",
  "SteÃ¼rsatz": "Steuersatz",
  "SteÃ¼rzahler": "Steuerzahler",
  "Fuehren": "FÃ¼hren",
  "Vermoegen": "VermÃ¶gen",
  "ueber": "Ã¼ber",
  "ae": "Ã¤",
  "oe": "Ã¶",
  "ue": "Ã¼",
};

// Hilfsfunktion: WÃ¶rterbuch automatisch erweitern
function addToDictionary(word) {
  if (!dictionary.words.includes(word)) {
    dictionary.words.push(word);
    fs.writeFileSync(dictionaryPath, JSON.stringify(dictionary, null, 2));
    console.log(`ðŸ“– Neues Wort ins WÃ¶rterbuch aufgenommen: ${word}`);
  }
}

// Content-Dateien prÃ¼fen
const files = fs.readdirSync("./content");

files.forEach((file) => {
  if (!file.endsWith(".md")) return;

  let content = fs.readFileSync(`./content/${file}`, "utf-8");

  // Feste Korrekturen anwenden
  for (const [wrong, correct] of Object.entries(replacements)) {
    if (content.includes(wrong)) {
      content = content.replaceAll(wrong, correct);
      console.log(`âœ… ${file}: "${wrong}" â†’ "${correct}" ersetzt`);
    }
  }

  // WÃ¶rter durchgehen und ins WÃ¶rterbuch aufnehmen
  const words = content.split(/\W+/);
  words.forEach((word) => {
    if (word.length > 3 && !dictionary.words.includes(word)) {
      addToDictionary(word);
    }
  });

  fs.writeFileSync(`./content/${file}`, content);
});
